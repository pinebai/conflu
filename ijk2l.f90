INTEGER FUNCTION ijk2l(i,j,k,ni,nj)

  IMPLICIT NONE

! ******************************************************************************
! Declarations and definitions
! ******************************************************************************

! ==============================================================================
! Arguments
! ==============================================================================

  INTEGER, INTENT(IN) :: i,j,k,ni,nj

! ==============================================================================
! Local variables
! ==============================================================================

! ******************************************************************************
! Start
! ******************************************************************************

  ijk2l = i + ni*(j-1) + ni*nj*(k-1)


! ******************************************************************************
! End
! ******************************************************************************

END FUNCTION ijk2l
